
public class operaciones {

	private int a;
	private int b;
	private int c;
	private int d;
	
	public operaciones (int a, int b, int c, int d){
		this.a = a;
		this.b = b;
		this.c = c;
		this.d = d;
	}
	public int suma(){
		return a+b;
	}
	public int resta(){
		return a-b;
	}
	public int multiplicación(){
		return a*b;
	}
	public int división(){
		return a/b;
	}
}
